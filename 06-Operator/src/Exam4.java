
public class Exam4 {
	public static void main(String[] args) {
		//증감연산자
		int num1 = 1;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		//변수값 1 증가
		num1 = num1 + 1;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		num1 += 1;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		num1++;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		++num1;
		System.out.println("num1 = "+ num1);
		System.out.println("------구분-----");
		
		
		//변수값 1 감소
		num1 = num1 - 1;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		num1 -= 1;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		num1--;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		--num1;
		System.out.println("num1 = "+ num1);
		System.out.println("------------");
		
		
		
	}

}
