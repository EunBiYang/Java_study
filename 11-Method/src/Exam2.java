
public class Exam2 {
	//더하기 함수
	static void plus(int x, int y) {
		int r = x + y;
		System.out.println(x +"+" +y+"="+r);
	}
	//빼기 함수
	static void minus(int x, int y) {
		int r = x - y;
		System.out.println(x +"+" +y+"="+r);
	}
	
	public static void main(String[] args) {
		plus(100,200);
		minus(100,200);
	}

}
